///////////////////////////////////////     空合运算符(??)    //////////////////////////////////////
//(a ?? b)  将可选类型进行空判断  如果包含一个值就进行解封, 否则就返回一个默认值b 空合运算符的两个条件:
            //1.a必须是可选类型
            //2.默认值b的类型必须和a存储值的类型保持一致
//空合运算符是以下代码的简写
                    //  a != nil ? a! : b


let defaultColorName = "red"
var userDefinedColorName:String? //= "blue"
var colorNameToUse = userDefinedColorName ?? defaultColorName


///////////////////////////////////////     区间运算符      //////////////////////////////////////
//闭区间运算符(a...b)  a到b(包含a和b)
for index in 1...5 {
    print(index)
}

//半开区间(a..<b)  a到b(包含a但不包含b)
for index in 1..<5 {
    print(index)
}


