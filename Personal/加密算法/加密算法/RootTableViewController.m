@interface RootTableViewController ()

@property (nonatomic, strong) NSMutableArray *dataArray;

@end

@implementation RootTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
